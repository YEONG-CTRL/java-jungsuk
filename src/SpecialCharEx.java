public class SpecialCharEx {
    public static void main(String[] args) {
        System.out.println('\'');
        System.out.println("abc\t123\b456"); // \t는 tab , \b는 backspace
        System.out.println('\n');
        System.out.println("\"Hello\"");
        System.out.println("c:\\");
    }
}

/*
결과
'
abc	12456


"Hello"
c:\
 */